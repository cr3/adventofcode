"""Unit tests for year 2022, day 10."""

from functools import reduce
from textwrap import dedent

import pytest

from adventofcode.year2022.day10 import (
    StateStrength,
    part1,
    part2,
)


@pytest.mark.parametrize(
    'state, expected',
    [
        (StateStrength(), 0),
        (reduce(lambda s, _: s.tick(), range(19), StateStrength()), 0),
        (reduce(lambda s, _: s.tick(), range(20), StateStrength()), 20),
        (reduce(lambda s, _: s.tick(), range(59), StateStrength()), 20),
        (reduce(lambda s, _: s.tick(), range(60), StateStrength()), 80),
    ],
)
def test_state_strength(state, expected):
    assert state.total_strength == expected


